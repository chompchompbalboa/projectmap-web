//-----------------------------------------------------------------------------
// Imports
//-----------------------------------------------------------------------------
import axios from '@/api/axios'
import { Edge } from '@/store/edge'

//-----------------------------------------------------------------------------
// Create Edge
//-----------------------------------------------------------------------------
export const createEdge = (
  edge: Edge
) => {
  return axios.post<Edge>('/api/edge/', edge)
}

//-----------------------------------------------------------------------------
// Delete Edge
//-----------------------------------------------------------------------------
export const deleteEdge = (
  edgeId: Edge['id']
) => {
  return axios.delete<Edge>('/api/edge/' + edgeId)
}